    echo -e "${CYAN}Current run results: $OUTPUT_DIR${NC}"
    echo -e "${CYAN}SBOMs saved in: $SBOM_DIR (current directory)${NC}"
    echo -e "${CYAN}Comparison report: $OUTPUT_DIR/sbom_comparison_report.md${NC}"
    echo ""
    
    # Show timing results
    if [[ "$USE_EXISTING" == true ]]; then
        echo -e "${GREEN}âš¡ FAST SCANNING PERFORMANCE âš¡${NC}"
        echo -e "${YELLOW}Scanning time (existing SBOMs): ${scan_duration} seconds${NC}"
        echo -e "${YELLOW}Benefits: No image pulling, no SBOM generation - just pure vulnerability scanning!${NC}"
    else
        echo -e "${YELLOW}ðŸ“Š PERFORMANCE METRICS ðŸ“Š${NC}"
        echo -e "${YELLOW}Scanning time (after SBOM generation): ${scan_duration} seconds${NC}"
        echo -e "${YELLOW}Next run: Use --use-existing flag for fast scanning demonstration${NC}"
    fi
    echo ""
    
    echo -e "${YELLOW}Generated SBOMs:${NC}"
    for image in "${IMAGES[@]}"; do
        if [[ -n "${sbom_files[$image]}" ]]; then
            local display_name=$(get_display_name "$image")
            echo -e "${GREEN}âœ“ $display_name: ${sbom_files[$image]}${NC}"
        else
            local display_name=$(get_display_name "$image")
            echo -e "${RED}âœ— $display_name: SBOM generation failed${NC}"
        fi
    done
    echo ""
    echo -e "${YELLOW}Key Insights:${NC}"
    echo -e "${YELLOW}â€¢ SBOM-Based Scanning: More consistent results using standardized software inventory${NC}"
    echo -e "${YELLOW}â€¢ Scanner != Scanner: Even with same SBOM, different vulnerability databases and matching${NC}"
    echo -e "${YELLOW}â€¢ ALL vulnerabilities included: Critical, High, Medium, Low, Unknown/Negligible${NC}"
    echo -e "${YELLOW}â€¢ Unfixed vulnerabilities tracked for complete risk assessment${NC}"
    echo -e "${YELLOW}â€¢ Supply Chain Transparency: SBOMs provide complete component visibility${NC}"
    echo -e "${YELLOW}â€¢ Compliance Ready: SBOM generation supports regulatory requirements${NC}"
    if [[ "$USE_EXISTING" == true ]]; then
        echo -e "${YELLOW}â€¢ Speed Advantage: SBOM-based scanning is much faster than direct image scanning${NC}"
    fi
}

